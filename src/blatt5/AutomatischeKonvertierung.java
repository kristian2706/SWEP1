package blatt5;

public class AutomatischeKonvertierung {
	public static void main(String[] args) {
		int a = 100, b = 12;
		double c = 100, d = 12;
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a * d);
		System.out.println(a / d);
		System.out.println(c * d);
		System.out.println(c / d);
		System.out.println(c * b);
		System.out.println(c / b);

		// "Implizites Casting" oder "Automatische Konvertierung" zu einem Double.

	}
}
